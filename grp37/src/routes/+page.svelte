<h3>Project description</h3>
<p>
    This project focuses on creating data visualizations to enhance the visibility of SunCharge’s supply chain for eco-friendly 
    car batteries. The project involves analyzing datasets related to materials, inventory, sales, purchases and plant operations, 
    aiming to help in detecting potential issues and facilitate the identification of areas for improvement within the supply chain 
    framework. Through this data analysis, the project will provide data-driven insights to assist SunCharge in making informed decisions 
    and logistical optimization.
</p>


<h3>Project Guiding questions</h3>
<ol>
    <li>How can we assess whether to boost or reduce sales efforts in specific countries by analyzing order quantity trends by 
        customers’ countries, and its annual variations?</li>
    <li>How does actual production and delivery times across suppliers affect supply chain efficiency in terms of time,  and where 
        can we identify potential areas for improving supply chain efficiency?</li>
    <li>How does the sale of finished products compare across our different plants, based on the number of customers served as well 
        as orders across each quarter for the past few years?</li>
</ol>
